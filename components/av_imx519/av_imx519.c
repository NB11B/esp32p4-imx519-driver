esp_err_t av_imx519_init(i2c_master_bus_handle_t i2c_bus, av_imx519_handle_t *out_handle) {
    if (!i2c_bus || !out_handle) return ESP_ERR_INVALID_ARG;

    struct av_imx519_ctx_s *ctx = calloc(1, sizeof(struct av_imx519_ctx_s));
    if (!ctx) return ESP_ERR_NO_MEM;

    // Add IMX519 device
    i2c_device_config_t imx519_cfg = {
        .dev_addr_length = I2C_ADDR_BIT_LEN_7,
        .device_address  = AV_IMX519_I2C_ADDR,
        .scl_speed_hz    = 400000,
    };
    esp_err_t err = i2c_master_bus_add_device(i2c_bus, &imx519_cfg, &ctx->imx519_dev);
    if (err != ESP_OK) {
        ESP_LOGE(TAG, "Failed to add IMX519 I2C device");
        free(ctx);
        return err;
    }

    // Add DW9714 VCM device (optional, don't fail if not present)
    i2c_device_config_t vcm_cfg = {
        .dev_addr_length = I2C_ADDR_BIT_LEN_7,
        .device_address  = AV_DW9714_I2C_ADDR,
        .scl_speed_hz    = 400000,
    };
    i2c_master_bus_add_device(i2c_bus, &vcm_cfg, &ctx->vcm_dev);

    // Software reset
    write_reg(ctx->imx519_dev, 0x0103, 0x01);
    vTaskDelay(pdMS_TO_TICKS(10));

    // Apply common init
    err = write_table(ctx->imx519_dev, imx519_common_regs);
    if (err != ESP_OK) {
        ESP_LOGE(TAG, "Failed to apply common init table");
        i2c_master_bus_rm_device(ctx->imx519_dev);
        if (ctx->vcm_dev) i2c_master_bus_rm_device(ctx->vcm_dev);
        free(ctx);
        return err;
    }

    // Default to QQVGA mode
    err = write_table(ctx->imx519_dev, imx519_qqvga_regs);
    if (err != ESP_OK) {
        ESP_LOGE(TAG, "Failed to apply QQVGA init table");
        i2c_master_bus_rm_device(ctx->imx519_dev);
        if (ctx->vcm_dev) i2c_master_bus_rm_device(ctx->vcm_dev);
        free(ctx);
        return err;
    }
    ctx->current_mode = AV_IMX519_MODE_QQVGA;

    // Stream on
    write_reg(ctx->imx519_dev, 0x0100, 0x01);

    // Set VCM to infinity focus
    av_imx519_set_vcm_dac(ctx, 0);

    *out_handle = ctx;
    ESP_LOGI(TAG, "IMX519 initialized successfully");
    return ESP_OK;
}

esp_err_t av_imx519_deinit(av_imx519_handle_t handle) {
    if (!handle) return ESP_ERR_INVALID_ARG;
    write_reg(handle->imx519_dev, 0x0100, 0x00); // Stream off
    i2c_master_bus_rm_device(handle->imx519_dev);
    if (handle->vcm_dev) i2c_master_bus_rm_device(handle->vcm_dev);
    free(handle);
    return ESP_OK;
}

esp_err_t av_imx519_set_mode(av_imx519_handle_t handle, av_imx519_mode_t mode) {
    if (!handle) return ESP_ERR_INVALID_ARG;
    if (mode >= AV_IMX519_MODE_MAX) return ESP_ERR_INVALID_ARG;
    if (mode == handle->current_mode) return ESP_OK;

    write_reg(handle->imx519_dev, 0x0100, 0x00); // Stream off
    vTaskDelay(pdMS_TO_TICKS(20));

    const imx519_reg_t *table = NULL;
    switch (mode) {
        case AV_IMX519_MODE_QQVGA: table = imx519_qqvga_regs; break;
        case AV_IMX519_MODE_VGA:   table = imx519_vga_regs; break;
        case AV_IMX519_MODE_720P:  table = imx519_720p_regs; break;
        case AV_IMX519_MODE_1080P: table = imx519_1080p_regs; break;
        default: return ESP_ERR_INVALID_ARG;
    }

    esp_err_t err = write_table(handle->imx519_dev, table);
    if (err != ESP_OK) return err;

    write_reg(handle->imx519_dev, 0x0100, 0x01); // Stream on
    vTaskDelay(pdMS_TO_TICKS(50)); // Settle time

    handle->current_mode = mode;
    return ESP_OK;
}

esp_err_t av_imx519_set_vcm_dac(av_imx519_handle_t handle, uint16_t dac) {
    if (!handle || !handle->vcm_dev) return ESP_ERR_INVALID_STATE;
    dac &= 0x03FF; // 10-bit
    uint8_t buf[2] = {
        (uint8_t)((dac >> 4) & 0x3F),
        (uint8_t)((dac & 0x0F) << 4),
    };
    return i2c_master_transmit(handle->vcm_dev, buf, 2, 50);
}

esp_err_t av_imx519_set_exposure(av_imx519_handle_t handle, uint32_t lines) {
    if (!handle) return ESP_ERR_INVALID_ARG;
    // IMX519_REG_EXPOSURE = 0x0202 (16-bit)
    esp_err_t err = write_reg(handle->imx519_dev, 0x0202, (lines >> 8) & 0xFF);
    if (err == ESP_OK) {
        err = write_reg(handle->imx519_dev, 0x0203, lines & 0xFF);
    }
    return err;
}

esp_err_t av_imx519_set_analog_gain(av_imx519_handle_t handle, uint16_t gain) {
    if (!handle) return ESP_ERR_INVALID_ARG;
    if (gain > 960) gain = 960;
    // IMX519_REG_ANALOG_GAIN = 0x0204 (16-bit)
    esp_err_t err = write_reg(handle->imx519_dev, 0x0204, (gain >> 8) & 0xFF);
    if (err == ESP_OK) {
        err = write_reg(handle->imx519_dev, 0x0205, gain & 0xFF);
    }
    return err;
}

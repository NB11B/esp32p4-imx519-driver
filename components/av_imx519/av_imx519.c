static esp_err_t write_reg(i2c_master_dev_handle_t dev, uint16_t addr, uint8_t val) {
    uint8_t buf[3] = { (uint8_t)(addr >> 8), (uint8_t)(addr & 0xFF), val };
    return i2c_master_transmit(dev, buf, 3, 50);
}

static esp_err_t write_table(i2c_master_dev_handle_t dev, const imx519_reg_t *table) {
    for (size_t i = 0; table[i].addr != 0xFFFF; ++i) {
        esp_err_t err = write_reg(dev, table[i].addr, table[i].val);
        if (err != ESP_OK) return err;
    }
    return ESP_OK;
}

